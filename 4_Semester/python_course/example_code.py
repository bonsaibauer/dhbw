import numpy as np
from matplotlib import pyplot as plt
t = np.arange(0.0, 6.0, 0.1)
print("t= ",t)
s = 1 - np.tan(2 * np.pi * t)
s2 = t
fig = plt.figure("Verlauf")
fig.clf()
ax=fig.add_subplot(1,1,1)

ax.plot(t, s,"g",t,s,"rx",t,s2,"bx")
ax.grid()
ax.set_title("Test")
ax.set_xlabel("123")

plt.show()
